#include<stdio.h>

int main() {
  int price = 100;
  printf("price = %d\n", price);

  float temparature = 30.5;
  printf("temp = %f\n", temparature);

  double temp2 = 29.5;
  printf("temp2 = %lf\n", temp2); //to get more accuracy

  char character = 'f'; //to store single character
  printf("char = %c\n", character);

  return 0;
}
